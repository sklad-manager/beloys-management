async function main() {
    console.log(`Starting seed for ${serviceList.length} services...`);
    let count = 0;

    for (const item of serviceList) {
        const formatted = item.trim();
        if (!formatted) continue;

        const existing = await prisma.referenceItem.findFirst({
            where: { type: 'SERVICE', value: formatted }
        });

        if (!existing) {
            await prisma.referenceItem.create({
                data: {
                    type: 'SERVICE',
                    value: formatted
                }
            });
            count++;
        }
    }
    console.log(`Successfully added ${count} new services.`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
