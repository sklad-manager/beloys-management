async function main() {
    console.log(`Starting seed for ${colorList.length} colors...`);
    let count = 0;

    for (const item of colorList) {
        const formatted = item.trim();

        const existing = await prisma.referenceItem.findFirst({
            where: { type: 'COLOR', value: formatted }
        });

        if (!existing) {
            await prisma.referenceItem.create({
                data: {
                    type: 'COLOR',
                    value: formatted
                }
            });
            count++;
        }
    }
    console.log(`Successfully added ${count} new colors.`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
