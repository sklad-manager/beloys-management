async function main() {
    console.log(`Starting seed for ${shoeList.length} items...`);
    let count = 0;

    for (const item of shoeList) {
        // Normalize first letter to uppercase just in case, though list looks good
        const formatted = item.trim();

        const existing = await prisma.referenceItem.findFirst({
            where: { type: 'SHOE_TYPE', value: formatted }
        });

        if (!existing) {
            await prisma.referenceItem.create({
                data: {
                    type: 'SHOE_TYPE',
                    value: formatted
                }
            });
            count++;
        }
    }
    console.log(`Successfully added ${count} new items.`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
